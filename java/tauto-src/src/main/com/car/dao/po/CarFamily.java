package main.com.car.dao.po;

import java.io.Serializable;

/**
 * 车系
 * @author Zwen
 *
 */
public class CarFamily implements Serializable {
	
	private static final long serialVersionUID = 7678248867478794335L;
	private Integer carFamilyId;                //车系id
	private String carFamilyName;              //车系名称
	private Integer brandId;				//品牌id
	public Integer getCarFamilyId() {
		return carFamilyId;
	}
	public void setCarFamilyId(Integer carFamilyId) {
		this.carFamilyId = carFamilyId;
	}
	public String getCarFamilyName() {
		return carFamilyName;
	}
	public void setCarFamilyName(String carFamilyName) {
		this.carFamilyName = carFamilyName;
	}
	public Integer getBrandId() {
		return brandId;
	}
	public void setBrandId(Integer brandId) {
		this.brandId = brandId;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
}
