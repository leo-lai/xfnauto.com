package main.com.car.dao.po;

import java.io.Serializable;

/**
 * 车辆品牌颜色分类
 * @author Zwen
 *
 */
public class CarColour implements Serializable {
	private static final long serialVersionUID = 7678248867478794335L;
	
	private Integer carColourId;                //车系id
	private String carColourName;              //车系名称
//	private Integer brandId;				//品牌id （20171114修改为注释）
	private Integer familyId;            //车系ID
	private Boolean isDelete;              //是否已删除
	private String colourNumber;      //色号 例：#90998
	
	public Integer getCarColourId() {
		return carColourId;
	}
	public void setCarColourId(Integer carColourId) {
		this.carColourId = carColourId;
	}
	public String getCarColourName() {
		return carColourName;
	}
	public void setCarColourName(String carColourName) {
		this.carColourName = carColourName;
	}
	public Integer getFamilyId() {
		return familyId;
	}
	public void setFamilyId(Integer familyId) {
		this.familyId = familyId;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	public Boolean getIsDelete() {
		return isDelete;
	}
	public void setIsDelete(Boolean isDelete) {
		this.isDelete = isDelete;
	}
	public String getColourNumber() {
		return colourNumber;
	}
	public void setColourNumber(String colourNumber) {
		this.colourNumber = colourNumber;
	}
}
