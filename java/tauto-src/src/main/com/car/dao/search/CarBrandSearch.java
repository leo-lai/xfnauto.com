package main.com.car.dao.search;

import java.io.Serializable;

import main.com.frame.search.BaseSearch;
/**
 * 品牌
 * @author Zwen
 *
 */
public class CarBrandSearch extends BaseSearch{
	private static final long serialVersionUID = -1359679113053580471L;
	
	private Integer brandId;   				//品牌id
	private String brandCode;				//品牌编码
	private String brandName;				//品牌名称
	private String brandInitial;            //品牌首字母
	private String brandLabel;              //进口标识（I进口/D国产）
	private String imgUrl;                  //图片地址
	
	public Integer getBrandId() {
		return brandId;
	}
	public void setBrandId(Integer brandId) {
		this.brandId = brandId;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	public String getBrandCode() {
		return brandCode;
	}
	public void setBrandCode(String brandCode) {
		this.brandCode = brandCode;
	}
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	public String getBrandInitial() {
		return brandInitial;
	}
	public void setBrandInitial(String brandInitial) {
		this.brandInitial = brandInitial;
	}
	public String getBrandLabel() {
		return brandLabel;
	}
	public void setBrandLabel(String brandLabel) {
		this.brandLabel = brandLabel;
	}
	public String getImgUrl() {
		return imgUrl;
	}
	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}
}
