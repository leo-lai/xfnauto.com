package main.com.car.dao.search;

import java.io.Serializable;

import main.com.frame.search.BaseSearch;

/**
 * 车辆品牌 车大类-颜色的图片中间表
 * @author Zwen
 *
 */
public class CarColourImageSearch extends BaseSearch{
	
	private Integer carColourImageId;               //车颜色图片ID
	private Integer carColourId;                //颜色id
	private String imagePath;              //图片路径
	private Integer carsId;
	private Boolean isDelete;              //是否已删除
	
	public Integer getCarColourImageId() {
		return carColourImageId;
	}
	public void setCarColourImageId(Integer carColourImageId) {
		this.carColourImageId = carColourImageId;
	}
	public Integer getCarColourId() {
		return carColourId;
	}
	public void setCarColourId(Integer carColourId) {
		this.carColourId = carColourId;
	}
	public String getImagePath() {
		return imagePath;
	}
	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}
	public Integer getCarsId() {
		return carsId;
	}
	public void setCarsId(Integer carsId) {
		this.carsId = carsId;
	}
	public Boolean getIsDelete() {
		return isDelete;
	}
	public void setIsDelete(Boolean isDelete) {
		this.isDelete = isDelete;
	}
}
