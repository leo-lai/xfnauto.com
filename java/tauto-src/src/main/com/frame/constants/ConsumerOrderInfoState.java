package main.com.frame.constants;

public enum ConsumerOrderInfoState {
	
	CarDistributing(1,"待配车"),
	CarChecking(2,"待验车"),
	CarChangeApply(3,"换车申请"),
	CarChanging(4,"换车审核"),
	CarChanged(5,"已换车"),
	Consulting(6,"待协商"),
	CarChecked(7,"验车完成"),
	countermand(8,"已退订");
	
	private Integer code;
	private String msg;
	
	ConsumerOrderInfoState(Integer code,String msg){
		this.code = code;
		this.msg = msg;
	}

	public Integer getCode() {
		return code;
	}

	public String getMsg() {
		return msg;
	}
	
	

}
