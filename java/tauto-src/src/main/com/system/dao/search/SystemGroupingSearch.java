package main.com.system.dao.search;

import main.com.frame.search.BaseSearch;

public class SystemGroupingSearch extends BaseSearch{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/**
     * 主键
     */
    private Integer groupingId;

    /**
     * 父id
     */
    private Integer parentId;

    /**
     * 序号
     */
    private Integer seq;

    /**
     * 角色编码
     */
    private String groupingCode;

    /**
     * 角色名称
     */
    private String groupingName;

    /**
     * 角色描述
     */
    private String remarks;

    /**
     * 是否已删除
     */
    private Byte overDelete;

	public Integer getGroupingId() {
		return groupingId;
	}

	public void setGroupingId(Integer groupingId) {
		this.groupingId = groupingId;
	}

	public Integer getParentId() {
		return parentId;
	}

	public void setParentId(Integer parentId) {
		this.parentId = parentId;
	}

	public Integer getSeq() {
		return seq;
	}

	public void setSeq(Integer seq) {
		this.seq = seq;
	}

	public String getGroupingCode() {
		return groupingCode;
	}

	public void setGroupingCode(String groupingCode) {
		this.groupingCode = groupingCode;
	}

	public String getGroupingName() {
		return groupingName;
	}

	public void setGroupingName(String groupingName) {
		this.groupingName = groupingName;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public Byte getOverDelete() {
		return overDelete;
	}

	public void setOverDelete(Byte overDelete) {
		this.overDelete = overDelete;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}
}
